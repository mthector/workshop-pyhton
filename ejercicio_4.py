print(f"La suma de {a} mas {b} es {suma}")

resta = a - b
print(f"La resta de {a} menos {b} es {resta}")

dividir = a / b
print(f"La division de {a} entre {b} es {dividir}")

multiplicar = a * b
print(f"La multiplicacion de {a} por {b} es {multiplicar}")


